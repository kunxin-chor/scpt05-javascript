let x = 42;
x++; // post-increment
console.log(x);

++x; // pre-increment
console.log(x);

let y = 33;
console.log("y is ", y++);  // post-increment means addition is executed after the statement has finished
console.log("y =", y);

let z = 10;
console.log("z is ", ++z); // pre-increment means do the addition before the statement