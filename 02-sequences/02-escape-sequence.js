// all escape sequence with a backward slash \ 
// the \n escape sequence stands for the newline character
console.log("Dear sir,\nyour bill of $10,000 is due");

// the \t escape sequence stands for tab
console.log("Dear sir\n\tyour bill of $10,00 is due");

// we can also escape special characters so that JS will treat them
// as literal characters
console.log('I don\'t know'); // <-- \' tells JavaScript its part of the String, not part of the code

console.log("She said, \"I don't know what is going on\"");

